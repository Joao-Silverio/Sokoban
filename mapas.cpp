void imprimeMapa(int m[20][22], int x, int y) {
    ///Imprime o jogo: mapa e personagem.
        for(int i=0;i<20;i++){
            for(int j=0;j<22;j++){
                if(i==x && j==y){
                    cout<<char(2); //personagem
                } else {
                    switch (m[i][j]){
                        case 0: cout<<" "; break; //caminho
                        case 1: cout<<char(219); break; //parede
                        //default: cout<<"-"; //erro
                    } //fim switchs
                }
            }
            cout<<"\n";
        } //fim for mapa
}

